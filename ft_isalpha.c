/*
** 32 == 0b100000
** a == 0b1100001
** 96 = 0b1100000
** 96 - a = 100000000...
** 26 =   0b11010
*/

int	ft_isalpha(int c)
{
	return (((unsigned)c | 32) - 'a' < 26);
}
